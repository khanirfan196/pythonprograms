# Demonstration of Sets.


farms_animals = {"cow","sheep","hen"}
print(farms_animals)
print("*" * 40)

for animals in farms_animals:
    print(animals)

print("*" * 40)

# another way to create set

wild_animals = set(["crocodile", "lion", "panther", "jackle"])
print(wild_animals)

print("*" * 40)

for animals in wild_animals:
    print(animals)

print("*" * 40)