# Program to manipulate alphabets of a string.
# Demo of Lower, Upper, Capitalize, Title, and Swapcase functions.
# Author : Irfan Khan Mohammed

str1 = "this is a simple string with no capital letters"
str2 = "THIS IS A SIMPLE STRING WITH NO LOWER LETTERS"
title = "this is title of an essay"
str3 = "first letter of first word is small"
str4 = "IRFAN khan"

print()
print(str1)
print("Upper case func:")
print(str1.upper())

print()
print(str2)
print(" lower case func:")
print(str2.lower())

print()
print(title)
print("Title func:")
print(title.title())

print()
print(str3)
print("Capitalize func:")
print(str3.capitalize())

print()
print(str4)
print("Swapcase func:")
print(str4.swapcase())